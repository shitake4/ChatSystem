package Entity;

public class MultipleInfo {

  private String title = null;
  private String value = null;

  // メソッド
  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }
}
